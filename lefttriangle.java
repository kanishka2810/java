class lefttriangle
{
    public static void main(String[] args)
    {
        for(int k=1;k<=5;k++)
        {
            for(int m=5-k;m>=1;m--)
            {
                System.out.print(" ");
            }
            for(int j=1;j<=k;j++)
            {
                System.out.print("*");
            }
        System.out.println();
        } 
    
    }
}